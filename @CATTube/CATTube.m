        %% - load
        
        function load(O,fullfile)
            
            if nargin < 2 || isempty(fullfile)
                % Allow an empty filename to get the filename from a GUI
                % window
                
                % Get the file name
                [FileName,PathName] = uigetfile('*.mat','Load .mat file',pwd);
                
                fullfile = [PathName FileName];
                
            end % if nargin
            
            % Send to CAT version of load
            load@CAT(O,fullfile)
            
        end % function
        
        %% - solvableCheck
        
        function validInput = solvableCheck(O,fieldnames)
            
            if ~iscell(fieldnames)
                fieldnames = {fieldnames};
            end
            validInput = zeros(size(fieldnames));
            i = 1;
            while i <= length(fieldnames) && ~any(validInput) 
                fieldname = fieldnames{i};
                if nargin>1
                    [validInput(i),solvableFlag] = O.diagnose(fieldname,O.(fieldname),'solvable');
                else
                    [~,solvableFlag] = O.diagnose([],[],'solvable');
                    validInput(i) = [];
                end

                if solvableFlag && isfield(O.gui,'run')
                    set(O.gui.run.start,'enable','on');
                elseif isfield(O.gui,'run')
                    set(O.gui.run.start,'enable','off');
                end
                i = i+1;
            end
            
            
        end % function
        
        function updateGUI(O)
           
            fieldnames = properties(O);
            fieldnames(strcmp(fieldnames,'gui')) = [];
            fieldnames(~cellfun(@isempty,strfind(fieldnames,'calc'))) = [];
            warning('off','all')
            for i = 1:length(fieldnames)
               eval(['O.',fieldnames{i},'_onset']); 
            end
